package chapter06_对象.object;

public class Point {
	private int x;
	private int y;

	public Point() {
		super();
	}

	public Point(int x, int y) {
		super();// 默认被调用
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	@Override
	public String toString() {
		return "(" + x + "," + y + ")";
	}

	@Override
	public boolean equals(Object obj) {
		// 实现处理参数obj的三种特殊情况
		if (obj == null)
			return false;
		if (obj == this)
			return true;
		if (!(obj instanceof Point)) {
			return false;
		} else {
			// 当前对象的x，y和参数对象的x，y是否相等
			Point p = (Point) obj;
			return this.x == p.x && this.y == p.y;
		}
	}
}
